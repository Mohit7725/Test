# Taking two inputs from the user
a = int(input("Enter the first number (a): "))
b = int(input("Enter the second number (b): "))

# 1. Floor Division
floor_division_result = a // b
print("Floor Division Result:", floor_division_result)

# 2. a power b
power_result = a ** b
print("a power b Result:", power_result)

# 3. Check if a and b are equal
equal_check = a == b
print("Are a and b equal?", equal_check)